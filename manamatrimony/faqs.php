<script>
    let currentOpenItem = null;

    const faqItems = document.querySelectorAll('.border-l-4');

    faqItems.forEach(item => {
        const button = item.querySelector('button');
        const answer = item.querySelector('p');

        button.addEventListener('click', () => {
            if (currentOpenItem && currentOpenItem !== item) {
                // Close the previously open answer
                currentOpenItem.querySelector('p').classList.add('hidden');
                currentOpenItem.querySelector('button svg').classList.remove('rotate-180');
            }

            // Toggle the clicked answer
            answer.classList.toggle('hidden');
            button.querySelector('svg').classList.toggle('rotate-180');

            // Update the currently open item
            currentOpenItem = item;
        });
    });
</script>

    
